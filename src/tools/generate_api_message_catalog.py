@dataclass(frozen=True)
class MessageDefinition:
    catalog_id: str
    message_id: str
    level: str
    status_code: int | None = None
    detail: str = ""
    summary: str = ""
    when: str = ""
    why_production: str = ""
    check_procedure: str = ""
    remediation_procedure: str = ""
    context_model: str = ""
    runbook: str = ""
    escalation: str = ""
    operator_action: str = ""
    source: str = ""
    tags: tuple[str, ...] = field(default_factory=tuple)
    emitted_from: tuple[str, ...] = field(default_factory=tuple)
    inferred: bool = False

    @property
    def sort_key(self) -> tuple[int, str, str]:
        if self.catalog_id:
            return (0, self.catalog_id, self.message_id)
        return (1, f"{LEVEL_ORDER.get(self.level, 999):03d}", self.message_id)

    @property
    def description(self) -> str:
        if self.when:
            return self.when
        if self.why_production:
            return self.why_production
        return self.summary

    def with_emitted_from(self, locations: Iterable[str]) -> MessageDefinition:
        current = set(self.emitted_from)
        current.update(locations)
        return replace(self, emitted_from=tuple(sorted(current)))

    def missing_required_fields(self) -> tuple[str, ...]:
        missing: list[str] = []
        for field_name in REQUIRED_FIELDS_BY_LEVEL.get(
            self.level, ()
        ):  # unknown level handled separately
            value = getattr(self, field_name)
            if not str(value).strip():
                missing.append(field_name)
        return tuple(missing)


