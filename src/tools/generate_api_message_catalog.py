def normalize_log_context_schema(value: Any) -> dict[str, str]:
    if not isinstance(value, Mapping):
        return {}
    context_schema = cast("Mapping[Any, Any]", value)
    schema: dict[str, str] = {}
    for raw_name, raw_definition in context_schema.items():
        name = str(raw_name).strip()
        if not name:
            continue
        if isinstance(raw_definition, Mapping):
            definition = cast("Mapping[str, Any]", raw_definition)
            description = as_display(definition.get("description"))
        else:
            description = as_display(raw_definition)
        if description.strip():
            schema[name] = description
    return schema


def field_description_from_call(call: ast.Call) -> str:
    if call_name(call) != "Field":
        return ""
    for keyword in call.keywords:
        if keyword.arg != "description":
            continue
        return as_display(literal_value(keyword.value))
    return ""


def pydantic_log_context_schema(module: ast.Module) -> dict[str, str]:
    model_names: set[str] = set()
    for node in ast.walk(module):
        if not isinstance(node, (ast.Assign, ast.AnnAssign)):
            continue
        value_node = node.value
        if not isinstance(value_node, ast.Name):
            continue
        target_names: set[str] = set()
        if isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name):
                    target_names.add(target.id)
        elif isinstance(node.target, ast.Name):
            target_names.add(node.target.id)
        if target_names.intersection(LOG_CONTEXT_SCHEMA_VARIABLE_NAMES):
            model_names.add(value_node.id)

    schema: dict[str, str] = {}
    for node in module.body:
        if not isinstance(node, ast.ClassDef) or node.name not in model_names:
            continue
        for statement in node.body:
            if not isinstance(statement, ast.AnnAssign):
                continue
            if not isinstance(statement.target, ast.Name):
                continue
            if not isinstance(statement.value, ast.Call):
                continue
            description = field_description_from_call(statement.value)
            if description:
                schema[log_context_alias_from_field_name(statement.target.id)] = description
    return schema


def api_error_resource_schema(api_dir: Path) -> dict[str, str]:
    module = read_python_ast(api_dir / "schemas.py")
    if module is None:
        return {}
    schema: dict[str, str] = {}
    for node in module.body:
        if not isinstance(node, ast.ClassDef) or node.name != "ErrorResource":
            continue
        for statement in node.body:
            if not isinstance(statement, ast.AnnAssign):
                continue
            if not isinstance(statement.target, ast.Name):
                continue
            if not isinstance(statement.value, ast.Call):
                continue
            description = field_description_from_call(statement.value)
            if description:
                schema[f"resource.{snake_to_lower_camel(statement.target.id)}"] = description
    return schema


def load_log_context_schema(root: Path) -> dict[str, str]:
    path = log_context_schema_path(root)
    module = read_python_ast(path)
    schema: dict[str, str] = {}
    if module is not None:
        schema.update(pydantic_log_context_schema(module))
        for node in ast.walk(module):
            value_node: ast.AST | None = None
            target_names: set[str] = set()
            if isinstance(node, ast.Assign):
                value_node = node.value
                for target in node.targets:
                    if isinstance(target, ast.Name):
                        target_names.add(target.id)
            elif isinstance(node, ast.AnnAssign):
                value_node = node.value
                if isinstance(node.target, ast.Name):
                    target_names.add(node.target.id)
            if value_node is not None and target_names.intersection(
                LOG_CONTEXT_SCHEMA_VARIABLE_NAMES
            ):
                schema.update(normalize_log_context_schema(literal_value(value_node)))
    return {**default_log_context_schema(), **schema}


def context_model_items(context_model: str) -> tuple[str, ...]:
    return tuple(item.strip() for item in context_model.split(",") if item.strip())


def context_field_description(name: str, schema: Mapping[str, str]) -> str:
    if name in schema:
        return schema[name]
    parts = name.split(".")
    for index in range(len(parts) - 1, 0, -1):
        wildcard = ".".join([*parts[:index], "*"])
        if wildcard in schema:
            return schema[wildcard]
    return f"`{name}` に出力されるログコンテキストです。"


def render_context_model_table(context_model: str, schema: Mapping[str, str]) -> list[str]:
    items = context_model_items(context_model)
    if not items:
        return [
            "#### 出力項目",
            "",
            "出力項目は定義されていません。",
            "",
        ]
    lines = [
        "#### 出力項目",
        "",
        "| 出力項目 | 説明 |",
        "| :--- | :--- |",
    ]
    for item in items:
        lines.append(
            "| "
            + " | ".join(
                [
                    f"`{escape_markdown(item)}`",
                    escape_markdown(context_field_description(item, schema)),
                ]
            )
            + " |"
        )
    lines.append("")
    return lines


def read_python_ast(path: Path) -> ast.Module | None:
    try:
        source = path.read_text(encoding="utf-8")
    except OSError as exc:
        print(f"[WARN] cannot read {path}: {exc}", file=sys.stderr)
        return None
    try:
        return ast.parse(source, filename=str(path))
    except SyntaxError as exc:
        print(f"[WARN] skip syntax-error file {path}: {exc}", file=sys.stderr)
        return None


def extract_mapping_from_call(call: ast.Call) -> dict[str, Any]:
    data: dict[str, Any] = {}
    if call.args:
        data["message_id"] = literal_value(call.args[0])
    if len(call.args) > 1:
        data["level"] = literal_value(call.args[1])
    for keyword in call.keywords:
        if keyword.arg is None:
            continue
        key = FIELD_ALIASES.get(keyword.arg, keyword.arg)
        data[key] = literal_value(keyword.value)
    return data


def normalize_definition(
    data: Mapping[str, Any], source: str, *, inferred: bool = False
) -> MessageDefinition | None:
    normalized: dict[str, Any] = {}
    for raw_key, raw_value in data.items():
        key = FIELD_ALIASES.get(str(raw_key), str(raw_key))
        normalized[key] = raw_value

    message_id = normalize_message_id(
        normalized.get("message_id") or normalized.get("message") or normalized.get("name") or ""
    )
    if not message_id:
        return None

    tags_value = normalized.get("tags") or ()
    tags: tuple[str, ...]
    if isinstance(tags_value, str):
        tags = (tags_value,)
    elif isinstance(tags_value, Iterable):
        tag_items = cast("Iterable[Any]", tags_value)  # type: ignore[redundant-cast]
        tags = tuple(str(item) for item in tag_items)
    else:
        tags = ()

    level = normalize_level(normalized.get("level"), fallback="INFO")
    return MessageDefinition(
        catalog_id=normalize_catalog_id(normalized.get("catalog_id") or ""),
        message_id=message_id,
        level=level,
        status_code=normalize_status_code(normalized.get("status_code")),
        detail=as_display(normalized.get("detail")),
        summary=as_display(normalized.get("summary")),
        when=as_display(normalized.get("when")),
        why_production=as_display(normalized.get("why_production")),
        check_procedure=as_display(normalized.get("check_procedure")),
        remediation_procedure=as_display(normalized.get("remediation_procedure")),
        context_model=as_display(normalized.get("context_model")),
        runbook=as_display(normalized.get("runbook")),
        escalation=as_display(normalized.get("escalation")),
        operator_action=as_display(normalized.get("operator_action")),
        source=source,
        tags=tags,
        inferred=inferred,
    )


def definitions_from_literal(value: Any, source: str) -> list[MessageDefinition]:
    definitions: list[MessageDefinition] = []
    if isinstance(value, list):
        list_items = cast("list[Any]", value)  # type: ignore[redundant-cast]
        for item in list_items:
            if isinstance(item, Mapping):
                definition = normalize_definition(cast("Mapping[str, Any]", item), source)
                if definition:
                    definitions.append(definition)
    elif isinstance(value, Mapping):
        mapping_items = cast("Mapping[Any, Any]", value)  # type: ignore[redundant-cast]
        for key, item in mapping_items.items():
            if isinstance(item, Mapping):
                data: dict[str, Any] = dict(cast("Mapping[str, Any]", item))
                data.setdefault("message_id", key)
                definition = normalize_definition(data, source)
                if definition:
                    definitions.append(definition)
            elif isinstance(item, str):
                definition = normalize_definition({"message_id": key, "summary": item}, source)
                if definition:
                    definitions.append(definition)
    return definitions


def parse_catalog_file(path: Path, root: Path) -> list[MessageDefinition]:
    module = read_python_ast(path)
    if module is None:
        return []
    definitions: list[MessageDefinition] = []
    source = repo_relative(path, root)
    for node in ast.walk(module):
        value_node: ast.AST | None = None
        target_names: set[str] = set()
        if isinstance(node, ast.Assign):
            value_node = node.value
            for target in node.targets:
                if isinstance(target, ast.Name):
                    target_names.add(target.id)
        elif isinstance(node, ast.AnnAssign):
            value_node = node.value
            if isinstance(node.target, ast.Name):
                target_names.add(node.target.id)

        if value_node is not None and target_names.intersection(CATALOG_VARIABLE_NAMES):
            definitions.extend(definitions_from_literal(literal_value(value_node), source))

        if isinstance(node, ast.Call) and call_name(node) in CATALOG_CALL_NAMES:
            definition = normalize_definition(
                extract_mapping_from_call(node), f"{source}:{node.lineno}"
            )
            if definition:
                definitions.append(definition)
    return definitions


def parse_route_meta(router_path: Path, root: Path, api_dir: Path) -> list[ApiMeta]:
    domain = api_dir.parent.name
    api = api_dir.name
    module = read_python_ast(router_path)
    if module is None:
        return [ApiMeta(domain, api, api, "", "", repo_relative(router_path, root))]

    metas: list[ApiMeta] = []
    for node in ast.walk(module):
        if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            continue
        for decorator in node.decorator_list:
            if not isinstance(decorator, ast.Call):
                continue
            func = decorator.func
            if not isinstance(func, ast.Attribute) or func.attr not in ROUTE_METHODS:
                continue
            path_value = literal_value(decorator.args[0]) if decorator.args else ""
            operation_id_node = get_keyword(decorator, ("operation_id", "operationId"))
            operation_id = (
                as_display(literal_value(operation_id_node)) if operation_id_node else node.name
            )
            metas.append(
                ApiMeta(
                    domain=domain,
                    api=api,
                    operation_id=operation_id or node.name,
                    method=func.attr.upper(),
                    path=as_display(path_value),
                    router_source=f"{repo_relative(router_path, root)}:{node.lineno}",
                )
            )
    if not metas:
        metas.append(ApiMeta(domain, api, api, "", "", repo_relative(router_path, root)))
    return metas


def message_id_from_call(call: ast.Call) -> str | None:
    kw_node = get_keyword(call, MESSAGE_ID_KW_NAMES)
    if kw_node is not None:
        value = literal_value(kw_node)
        if isinstance(value, str) and value.strip():
            return normalize_message_id(value)
    for arg in call.args[:2]:
        if (
            isinstance(arg, ast.Call)
            and dotted_name(arg.func) == "router_error_message_id"
            and arg.args
        ):
            operation_id = literal_value(arg.args[0])
            if isinstance(operation_id, str) and operation_id.strip():
                return normalize_message_id(f"{operation_id}.router_api_function_error")
        value = literal_value(arg)
        if (
            isinstance(value, str)
            and value.strip()
            and re.match(r"^[A-Za-z][A-Za-z0-9_.:-]*$", value)
        ):
            return normalize_message_id(value)
    return None


def catalog_id_from_call(call: ast.Call) -> str | None:
    kw_node = get_keyword(call, CATALOG_ID_FIELD_NAMES)
    if kw_node is None:
        return None
    value = literal_value(kw_node)
    if isinstance(value, str) and value.strip():
        return normalize_catalog_id(value)
    return None


def level_hint_from_wrapper_method(method_name: str, call: ast.Call) -> str | None:
    direct = WRAPPER_METHOD_LEVELS.get(method_name)
    if direct:
        return direct
    level_node = get_keyword(call, LEVEL_KW_NAMES)
    if level_node is not None:
        return normalize_level(literal_value(level_node))
    if method_name == "exception":
        return "ERROR"
    return None


def literal_context_keys(call: ast.Call) -> tuple[str, ...]:
    context_node = get_keyword(call, ("context", "extra", "payload"))
    if (
        isinstance(context_node, ast.Call)
        and dotted_name(context_node.func) == "router_log_context"
    ):
        return router_log_context_keys(context_node)
    value = literal_value(context_node)
    if isinstance(value, Mapping):
        mapping = cast("Mapping[Any, Any]", value)  # type: ignore[redundant-cast]
        return tuple(sorted(str(key) for key in mapping))
    return ()


def router_log_context_keys(call: ast.Call) -> tuple[str, ...]:
    keys = {"api.statusCode", "error.code", "error.message"}
    if get_keyword(call, ("caller",)) is not None:
        keys.add("actorPrincipalId")
    if get_keyword(call, ("request_context",)) is not None:
        keys.update(("traceId", "request.sourceIp", "request.userAgent", "request.actorType"))
    if get_keyword(call, ("error",)) is not None:
        keys.add("error.exceptionType")

    resource_node = get_keyword(call, ("resource",))
    if isinstance(resource_node, ast.Dict):
        for key_node in resource_node.keys:
            key_value = literal_value(key_node)
            if isinstance(key_value, str) and key_value:
                keys.add(f"resource.{key_value}")
    return tuple(sorted(keys))


def catalog_fields_from_call(call: ast.Call) -> dict[str, Any]:
    fields: dict[str, Any] = {}
    for keyword in call.keywords:
        if keyword.arg is None:
            continue
        if keyword.arg in {"context", "extra", "payload"}:
            continue
        key = FIELD_ALIASES.get(keyword.arg, keyword.arg)
        if key in {
            "catalog_id",
            "message_id",
            "level",
            "summary",
            "status_code",
            "detail",
            "when",
            "why_production",
            "check_procedure",
            "remediation_procedure",
            "context_model",
            "operator_action",
            "runbook",
            "escalation",
            "tags",
        }:
            if key == "summary" and isinstance(keyword.value, ast.Call):
                fields[key] = router_error_api_function_summary(keyword.value)
            else:
                fields[key] = literal_value(keyword.value)
    return fields


def router_error_api_function_summary(call: ast.Call) -> str | None:
    if dotted_name(call.func) != "router_error_summary" or not call.args:
        return None
    summary = literal_value(call.args[0])
    if not isinstance(summary, str):
        return None
    return summary.replace(
        "Routerで捕捉した例外により",
        "Routerで捕捉したApiFunctionErrorにより",
    )


def is_dotted_call(node: ast.Call, modules: set[str], names: set[str]) -> bool:
    dotted = dotted_name(node.func)
    if not dotted:
        return False
    for module_name in modules:
        for name in names:
            if dotted == f"{module_name}.{name}":
                return True
    return False


class WrapperCallVisitor(ast.NodeVisitor):
    def __init__(self, path: Path, root: Path) -> None:
        self.path = path
        self.root = root
        self.current_function: str | None = None
        self.factory_names: set[str] = set(WRAPPER_FACTORY_NAMES)
        self.helper_names: set[str] = set(WRAPPER_HELPER_NAMES)
        self.module_names: set[str] = set()
        self.wrapper_variables: set[str] = set(WRAPPER_VARIABLE_HINTS)
        self.calls: list[WrapperCall] = []

    def visit_Import(self, node: ast.Import) -> None:
        for alias in node.names:
            if alias.name in WRAPPER_MODULES:
                self.module_names.add(alias.asname or alias.name.split(".")[-1])
        self.generic_visit(node)

    def visit_ImportFrom(self, node: ast.ImportFrom) -> None:
        module = node.module or ""
        if module in WRAPPER_MODULES:
            for alias in node.names:
                imported_name = alias.name
                local_name = alias.asname or alias.name
                if imported_name in WRAPPER_FACTORY_NAMES:
                    self.factory_names.add(local_name)
                if imported_name in WRAPPER_HELPER_NAMES:
                    self.helper_names.add(local_name)
                if imported_name == "OperationalLogger":
                    self.wrapper_variables.add(local_name)
        if module == "app.core":
            for alias in node.names:
                if alias.name == "logging":
                    self.module_names.add(alias.asname or alias.name)
        self.generic_visit(node)

    def visit_FunctionDef(self, node: ast.FunctionDef) -> None:
        previous = self.current_function
        self.current_function = node.name
        self.generic_visit(node)
        self.current_function = previous

    def visit_AsyncFunctionDef(self, node: ast.AsyncFunctionDef) -> None:
        previous = self.current_function
        self.current_function = node.name
        self.generic_visit(node)
        self.current_function = previous

    def visit_Assign(self, node: ast.Assign) -> None:
        if isinstance(node.value, ast.Call) and self._is_wrapper_factory_call(node.value):
            for target in node.targets:
                for name in assigned_names(target):
                    self.wrapper_variables.add(name)
        self.generic_visit(node)

    def visit_AnnAssign(self, node: ast.AnnAssign) -> None:
        if isinstance(node.value, ast.Call) and self._is_wrapper_factory_call(node.value):
            for name in assigned_names(node.target):
                self.wrapper_variables.add(name)
        self.generic_visit(node)

    def visit_Call(self, node: ast.Call) -> None:
        wrapper_name: str | None = None
        level_hint: str | None = None
        if isinstance(node.func, ast.Name) and node.func.id in self.helper_names:
            wrapper_name = node.func.id
            level_hint = level_hint_from_wrapper_method(node.func.id, node)
        elif isinstance(node.func, ast.Attribute):
            receiver = dotted_name(node.func.value)
            if (
                isinstance(node.func.value, ast.Name)
                and node.func.value.id in self.wrapper_variables
            ):
                if node.func.attr in WRAPPER_METHOD_LEVELS:
                    wrapper_name = f"{node.func.value.id}.{node.func.attr}"
                    level_hint = level_hint_from_wrapper_method(node.func.attr, node)
            elif receiver in self.module_names and node.func.attr in WRAPPER_HELPER_NAMES:
                wrapper_name = f"{receiver}.{node.func.attr}"
                level_hint = level_hint_from_wrapper_method(node.func.attr, node)
        if wrapper_name:
            self.calls.append(
                WrapperCall(
                    catalog_id=catalog_id_from_call(node),
                    message_id=message_id_from_call(node),
                    level_hint=level_hint,
                    source=f"{repo_relative(self.path, self.root)}:{node.lineno}",
                    function_name=self.current_function,
                    wrapper=wrapper_name,
                    context_keys=literal_context_keys(node),
                    catalog_fields=catalog_fields_from_call(node),
                )
            )
        self.generic_visit(node)

    def _is_wrapper_factory_call(self, call: ast.Call) -> bool:
        if isinstance(call.func, ast.Name) and call.func.id in self.factory_names:
            return True
        if isinstance(call.func, ast.Attribute):
            receiver = dotted_name(call.func.value)
            if receiver in self.module_names and call.func.attr in WRAPPER_FACTORY_NAMES:
                return True
        return is_dotted_call(call, WRAPPER_MODULES, WRAPPER_FACTORY_NAMES)


def assigned_names(node: ast.AST) -> set[str]:
    if isinstance(node, ast.Name):
        return {node.id}
    if isinstance(node, (ast.Tuple, ast.List)):
        names: set[str] = set()
        for element in node.elts:
            names.update(assigned_names(element))
        return names
    return set()


def api_python_files(api_dir: Path) -> list[Path]:
    paths: list[Path] = []
    for path in sorted(api_dir.rglob("*.py")):
        parts = set(path.parts)
        if "generated" in parts or "__pycache__" in parts:
            continue
        if path.name in {"schemas.py", "samples.py", "contract.py", "__init__.py"}:
            continue
        paths.append(path)
    return paths


def parse_wrapper_calls(api_dir: Path, root: Path) -> list[WrapperCall]:
    calls: list[WrapperCall] = []
    for path in api_python_files(api_dir):
        if path.name in CATALOG_FILENAMES:
            continue
        module = read_python_ast(path)
        if module is None:
            continue
        visitor = WrapperCallVisitor(path, root)
        visitor.visit(module)
        calls.extend(visitor.calls)
    return calls


class DirectLoggerVisitor(ast.NodeVisitor):
    def __init__(self, path: Path, root: Path) -> None:
        self.path = path
        self.root = root
        self.logging_module_aliases: set[str] = set()
        self.logging_factory_aliases: set[str] = set()
        self.logging_function_aliases: set[str] = set()
        self.logger_variables: set[str] = set(LOGGER_VARIABLE_NAMES)
        self.wrapper_factory_names: set[str] = set(WRAPPER_FACTORY_NAMES)
        self.wrapper_helper_names: set[str] = set(WRAPPER_HELPER_NAMES)
        self.wrapper_module_names: set[str] = set()
        self.wrapper_variables: set[str] = set(WRAPPER_VARIABLE_HINTS)
        self.violations: list[DirectLoggerViolation] = []

    def visit_Import(self, node: ast.Import) -> None:
        for alias in node.names:
            top = alias.name.split(".")[0]
            local = alias.asname or top
            if alias.name in WRAPPER_MODULES:
                self.wrapper_module_names.add(alias.asname or alias.name.split(".")[-1])
                continue
            if top in FORBIDDEN_LOG_MODULES:
                self.logging_module_aliases.add(local)
                self._add(node, "forbidden-import", f"direct import of {alias.name!r}")
        self.generic_visit(node)

    def visit_ImportFrom(self, node: ast.ImportFrom) -> None:
        module = node.module or ""
        if module in WRAPPER_MODULES:
            for alias in node.names:
                local_name = alias.asname or alias.name
                if alias.name in WRAPPER_FACTORY_NAMES:
                    self.wrapper_factory_names.add(local_name)
                if alias.name in WRAPPER_HELPER_NAMES:
                    self.wrapper_helper_names.add(local_name)
            self.generic_visit(node)
            return
        if module == "app.core":
            for alias in node.names:
                if alias.name == "logging":
                    self.wrapper_module_names.add(alias.asname or alias.name)
            self.generic_visit(node)
            return
        if module.split(".")[0] in FORBIDDEN_LOG_MODULES:
            for alias in node.names:
                local = alias.asname or alias.name
                if alias.name in LOGGER_FACTORY_METHODS:
                    self.logging_factory_aliases.add(local)
                if alias.name in LOGGER_METHODS:
                    self.logging_function_aliases.add(local)
                self._add(node, "forbidden-import", f"direct import from {module!r}: {alias.name}")
        self.generic_visit(node)

    def visit_Assign(self, node: ast.Assign) -> None:
        if isinstance(node.value, ast.Call):
            if self._is_wrapper_factory_call(node.value):
                for target in node.targets:
                    self.wrapper_variables.update(assigned_names(target))
            if self._is_logging_factory_call(node.value):
                for target in node.targets:
                    names = assigned_names(target)
                    self.logger_variables.update(names)
                    for name in names:
                        self._add(node, "direct-logger-factory", f"{name} = logging.getLogger(...)")
        self.generic_visit(node)

    def visit_AnnAssign(self, node: ast.AnnAssign) -> None:
        if isinstance(node.value, ast.Call):
            if self._is_wrapper_factory_call(node.value):
                self.wrapper_variables.update(assigned_names(node.target))
            if self._is_logging_factory_call(node.value):
                names = assigned_names(node.target)
                self.logger_variables.update(names)
                for name in names:
                    self._add(node, "direct-logger-factory", f"{name} = logging.getLogger(...)")
        self.generic_visit(node)

    def visit_Call(self, node: ast.Call) -> None:
        if self._is_logging_factory_call(node):
            self._add(node, "direct-logger-factory", "logging.getLogger(...) is not allowed")
        if isinstance(node.func, ast.Name):
            if node.func.id in self.logging_function_aliases:
                self._add(node, "direct-logger-call", f"{node.func.id}(...) is not allowed")
        elif isinstance(node.func, ast.Attribute):
            receiver = dotted_name(node.func.value)
            if (
                receiver in self.logging_module_aliases
                and node.func.attr in LOGGER_METHODS | LOGGER_FACTORY_METHODS
            ):
                self._add(
                    node, "direct-logger-call", f"{receiver}.{node.func.attr}(...) is not allowed"
                )
            if isinstance(node.func.value, ast.Name):
                base_name = node.func.value.id
                if base_name in self.wrapper_variables:
                    self.generic_visit(node)
                    return
                if base_name in self.logger_variables and node.func.attr in LOGGER_METHODS:
                    self._add(
                        node,
                        "direct-logger-call",
                        f"{base_name}.{node.func.attr}(...) is not allowed",
                    )
        self.generic_visit(node)

    def _is_logging_factory_call(self, call: ast.Call) -> bool:
        if isinstance(call.func, ast.Name) and call.func.id in self.logging_factory_aliases:
            return True
        if isinstance(call.func, ast.Attribute):
            receiver = dotted_name(call.func.value)
            if receiver in self.logging_module_aliases and call.func.attr in LOGGER_FACTORY_METHODS:
                return True
        return False

    def _is_wrapper_factory_call(self, call: ast.Call) -> bool:
        if isinstance(call.func, ast.Name) and call.func.id in self.wrapper_factory_names:
            return True
        if isinstance(call.func, ast.Attribute):
            receiver = dotted_name(call.func.value)
            if receiver in self.wrapper_module_names and call.func.attr in WRAPPER_FACTORY_NAMES:
                return True
        return False

    def _add(self, node: ast.AST, kind: str, detail: str) -> None:
        self.violations.append(
            DirectLoggerViolation(
                path=self.path,
                line=getattr(node, "lineno", 0),
                kind=kind,
                detail=detail,
            )
        )


def iter_python_files(scan_root: Path) -> Iterable[Path]:
    if not scan_root.exists():
        return []
    skip_parts = {".venv", "venv", "__pycache__", "generated", ".mypy_cache", ".ruff_cache"}
    paths: list[Path] = []
    for path in sorted(scan_root.rglob("*.py")):
        if set(path.parts).intersection(skip_parts):
            continue
        paths.append(path)
    return paths


def find_direct_logger_violations(
    *,
    root: Path,
    scan_root: Path,
    allowed_files: Sequence[str],
) -> list[DirectLoggerViolation]:
    allowed = {
        ((root / item).resolve() if not Path(item).is_absolute() else Path(item).resolve())
        for item in allowed_files
    }
    violations: list[DirectLoggerViolation] = []
    for path in iter_python_files(scan_root):
        if path.resolve() in allowed:
            continue
        module = read_python_ast(path)
        if module is None:
            continue
        visitor = DirectLoggerVisitor(path, root)
        visitor.visit(module)
        violations.extend(visitor.violations)
    return violations


