def repo_relative(path: Path, root: Path) -> str:
    try:
        return path.resolve().relative_to(root.resolve()).as_posix()
    except ValueError:
        return path.as_posix()


def escape_markdown(value: Any) -> str:
    text = "" if value is None else str(value)
    text = text.replace("\n", "<br>")
    return text.replace("|", "\\|")


def normalize_level(value: Any, fallback: str = "INFO") -> str:
    if value is None:
        return fallback
    text = str(value).strip()
    if not text:
        return fallback
    text = text.split(".")[-1].upper()
    if text == "WARN":
        return "WARNING"
    if text not in LEVEL_ORDER:
        return fallback
    return text


def normalize_message_id(value: Any) -> str:
    text = str(value).strip()
    text = text.replace(" ", "_")
    return text


def normalize_catalog_id(value: Any) -> str:
    text = str(value).strip()
    return text


def normalize_status_code(value: Any) -> int | None:
    if isinstance(value, int):
        return value
    text = str(value).strip()
    if not text:
        return None
    if text.isdecimal():
        return int(text)
    match = re.search(r"HTTP_(?P<code>[0-9]{3})_", text)
    if match is not None:
        return int(match.group("code"))
    return None


def get_keyword(call: ast.Call, names: Sequence[str]) -> ast.AST | None:
    wanted = set(names)
    for keyword in call.keywords:
        if keyword.arg in wanted:
            return keyword.value
    return None


def call_name(node: ast.Call) -> str:
    func = node.func
    if isinstance(func, ast.Name):
        return func.id
    if isinstance(func, ast.Attribute):
        return func.attr
    return ""


def dotted_name(node: ast.AST) -> str | None:
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        base = dotted_name(node.value)
        if base:
            return f"{base}.{node.attr}"
        return node.attr
    return None


def literal_value(node: ast.AST | None) -> Any:
    """Best-effort conversion of AST expressions into stable display values."""
    if node is None:
        return None
    if isinstance(node, ast.Constant):
        return node.value
    if isinstance(node, (ast.List, ast.Tuple, ast.Set)):
        return [literal_value(element) for element in node.elts]
    if isinstance(node, ast.Dict):
        converted: dict[str, Any] = {}
        for key, value in zip(node.keys, node.values, strict=False):
            if key is None:
                continue
            key_value = literal_value(key)
            if key_value is None:
                continue
            converted[str(key_value)] = literal_value(value)
        return converted
    if isinstance(node, ast.Name):
        return node.id
    if isinstance(node, ast.Attribute):
        return dotted_name(node)
    if isinstance(node, ast.Call) and isinstance(node.func, ast.Name) and node.func.id == "dict":
        return {
            keyword.arg: literal_value(keyword.value) for keyword in node.keywords if keyword.arg
        }
    if isinstance(node, ast.Call) and call_name(node) in LOG_CONTEXT_MODEL_CALL_NAMES:
        arg_aliases = [
            log_context_alias_from_field_name(str(literal_value(arg)))
            for arg in node.args
            if isinstance(literal_value(arg), str)
        ]
        keyword_aliases = [
            log_context_alias_from_field_name(keyword.arg)
            for keyword in node.keywords
            if keyword.arg
        ]
        return ", ".join([*arg_aliases, *keyword_aliases])
    try:
        return ast.unparse(node)
    except Exception:  # pragma: no cover - defensive for malformed ASTs
        return "<expr>"


def as_display(value: Any) -> str:
    if value is None:
        return ""
    if isinstance(value, str):
        return value
    if isinstance(value, (list, tuple, set)):
        items = cast("Iterable[Any]", value)
        return ", ".join(as_display(item) for item in items)
    if isinstance(value, Mapping):
        return json.dumps(
            dict(cast("Mapping[str, Any]", value)), ensure_ascii=False, sort_keys=True
        )
    return str(value)


def log_context_alias_from_field_name(field_name: str) -> str:
    dotted_prefixes = {"api", "resource", "aws", "metrics", "error"}
    prefix, separator, rest = field_name.partition("_")
    if separator and prefix in dotted_prefixes:
        return f"{prefix}.{snake_to_lower_camel(rest)}"
    return snake_to_lower_camel(field_name)


def snake_to_lower_camel(value: str) -> str:
    parts = value.split("_")
    if not parts:
        return value
    return parts[0] + "".join(part[:1].upper() + part[1:] for part in parts[1:])


