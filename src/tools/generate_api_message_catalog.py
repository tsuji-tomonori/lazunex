"""Generate and verify API-level operational message catalogs for Lazunex.

This tool is intentionally static: it does not import application modules.  It
parses API directories with ``ast`` and performs three checks before generating
Markdown:

1. Application code does not call the stdlib logger directly.
2. API code emits logs only through the Lazunex wrapper in ``app.core.logging``.
3. Each wrapper call uses a message_id that is documented in the API message
   catalog when strict CI mode is enabled.

Default usage from the repository root::

    python src/tools/generate_api_message_catalog.py
    python src/tools/generate_api_message_catalog.py --check
    python src/tools/generate_api_message_catalog.py --strict --enforce-logger-wrapper

Expected API layout::

    src/app/apis/{domain}/{api}/
      router.py
      functions.py
      message_catalog.py   # recommended primary source

Generated outputs::

    docs/spec/40.apis/{domain}/{api}/messages_gen.md
    docs/spec/40.apis/messages_index_gen.md
"""

