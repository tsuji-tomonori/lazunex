def markdown_escape(value: str) -> str:
    return value.replace("|", "\\|").replace("\n", " ").strip()


def to_camel(value: str) -> str:
    parts = value.split("_")
    return parts[0] + "".join(part[:1].upper() + part[1:] for part in parts[1:])


def clean_docstring(value: str | None) -> str:
    if not value:
        return "-"
    return " ".join(value.strip().split())


def column_comment(tables: dict[str, Table], table: str, column: str) -> str:
    for ddl_column in tables.get(table, Table(name=table, columns=[])).columns:
        if ddl_column.name == column:
            return ddl_column.comment or "-"
    return "-"


def string_literal_from_joined(node: ast.AST | None) -> str | None:
    if isinstance(node, ast.Constant) and isinstance(node.value, str):
        return node.value
    if isinstance(node, ast.JoinedStr):
        return "".join(
            str(part.value)
            for part in node.values
            if isinstance(part, ast.Constant) and isinstance(part.value, str)
        )
    return None


def field_description(node: ast.AST | None) -> str:
    if not isinstance(node, ast.Call):
        return "-"
    description = string_literal_from_joined(keyword_value(node, "description"))
    return description or "-"


def schema_specs(schemas_path: Path) -> dict[str, SchemaSpec]:
    if not schemas_path.exists():
        return {}
    tree = ast.parse(schemas_path.read_text(encoding="utf-8"), filename=str(schemas_path))
    specs: dict[str, SchemaSpec] = {}
    for node in tree.body:
        if not isinstance(node, ast.ClassDef):
            continue
        fields: list[FieldSpec] = []
        for statement in node.body:
            if not isinstance(statement, ast.AnnAssign) or not isinstance(
                statement.target, ast.Name
            ):
                continue
            fields.append(
                FieldSpec(
                    name=to_camel(statement.target.id),
                    type_name=ast.unparse(statement.annotation),
                    description=field_description(statement.value),
                )
            )
        if fields:
            specs[node.name] = SchemaSpec(
                name=node.name,
                description=clean_docstring(ast.get_docstring(node)),
                fields=tuple(fields),
            )
    return specs


def strip_optional_type(type_name: str) -> str:
    return type_name.replace(" | None", "").replace("None | ", "")


def schema_field_rows(
    schema: SchemaSpec,
    specs: dict[str, SchemaSpec],
    *,
    prefix: str = "",
) -> list[FieldSpec]:
    rows: list[FieldSpec] = []
    for field in schema.fields:
        name = f"{prefix}.{field.name}" if prefix else field.name
        rows.append(FieldSpec(name=name, type_name=field.type_name, description=field.description))
        nested_name = strip_optional_type(field.type_name)
        if nested_name.startswith("list[") and nested_name.endswith("]"):
            nested_name = nested_name.removeprefix("list[").removesuffix("]")
        if nested_name in specs:
            rows.extend(schema_field_rows(specs[nested_name], specs, prefix=name))
    return rows


def endpoint_response_model(function: ast.AsyncFunctionDef | ast.FunctionDef) -> str | None:
    for decorator in function.decorator_list:
        if not isinstance(decorator, ast.Call):
            continue
        value = keyword_value(decorator, "response_model")
        if isinstance(value, ast.Name):
            return value.id
    return None


def endpoint_body_schema(function: ast.AsyncFunctionDef | ast.FunctionDef) -> str | None:
    for arg in function.args.args:
        annotation = arg.annotation
        if not isinstance(annotation, ast.Subscript):
            continue
        parts = annotation.slice.elts if isinstance(annotation.slice, ast.Tuple) else ()
        if not parts:
            continue
        if any(is_fastapi_marker(part, "Body") for part in parts[1:]):
            return ast.unparse(parts[0])
    return None


def is_fastapi_marker(node: ast.AST, name: str) -> bool:
    return isinstance(node, ast.Call) and (
        (isinstance(node.func, ast.Name) and node.func.id == name)
        or (isinstance(node.func, ast.Attribute) and node.func.attr == name)
    )


def marker_alias(node: ast.AST) -> str | None:
    if not isinstance(node, ast.Call):
        return None
    return literal_string(keyword_value(node, "alias"))


def marker_description(node: ast.AST) -> str:
    if not isinstance(node, ast.Call):
        return "-"
    return literal_string(keyword_value(node, "description")) or "-"


def endpoint_inputs(function: ast.AsyncFunctionDef | ast.FunctionDef) -> tuple[EndpointInput, ...]:
    inputs: list[EndpointInput] = []
    for arg in function.args.args:
        annotation = arg.annotation
        if not isinstance(annotation, ast.Subscript):
            continue
        parts = annotation.slice.elts if isinstance(annotation.slice, ast.Tuple) else ()
        if len(parts) < 2:
            continue
        type_name = ast.unparse(parts[0])
        for marker_name, location in (("Path", "path"), ("Header", "header"), ("Body", "body")):
            marker = next(
                (part for part in parts[1:] if is_fastapi_marker(part, marker_name)),
                None,
            )
            if marker is None:
                continue
            inputs.append(
                EndpointInput(
                    location=location,
                    name=marker_alias(marker) or to_camel(arg.arg),
                    type_name=type_name,
                    description=marker_description(marker),
                )
            )
    return tuple(inputs)


def endpoint_input_origins(function: ast.AsyncFunctionDef | ast.FunctionDef) -> dict[str, str]:
    origins: dict[str, str] = {}
    for arg in function.args.args:
        annotation = arg.annotation
        if not isinstance(annotation, ast.Subscript):
            continue
        parts = annotation.slice.elts if isinstance(annotation.slice, ast.Tuple) else ()
        if len(parts) < 2:
            continue
        for marker_name, location in (
            ("Path", "path"),
            ("Header", "header"),
            ("Body", "request.body"),
        ):
            marker = next(
                (part for part in parts[1:] if is_fastapi_marker(part, marker_name)),
                None,
            )
            if marker is None:
                continue
            name = marker_alias(marker) or to_camel(arg.arg)
            origins[arg.arg] = f"{location}.{name}" if location != "request.body" else location
    return origins


