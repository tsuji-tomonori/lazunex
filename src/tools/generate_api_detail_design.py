@dataclass(frozen=True)
class FieldSpec:
    name: str
    type_name: str
    description: str


@dataclass(frozen=True)
class SchemaSpec:
    name: str
    description: str
    fields: tuple[FieldSpec, ...]


@dataclass(frozen=True)
class EndpointInput:
    location: str
    name: str
    type_name: str
    description: str


@dataclass(frozen=True)
class SqlSourceValue:
    parameter: str
    source: str
    note_sql: str | None = None


@dataclass(frozen=True)
class SqlSpec:
    filename: str
    action: str
    table: str
    column_params: tuple[tuple[str, SqlSourceValue], ...]


@dataclass(frozen=True)
class QuerySpec:
    function_name: str
    params_class: str
    row_class: str
    sql_filename: str
    summary: str
    tables: tuple[str, ...]


@dataclass(frozen=True)
class ResourceChange:
    action: str
    table: str
    summary: str
    sql_filename: str
    values: tuple[tuple[str, str, str, str], ...]
    notes: tuple[tuple[str, str], ...] = ()


@dataclass(frozen=True)
class ExternalResourceInput:
    class_name: str
    parameters: tuple[tuple[str, str], ...]


